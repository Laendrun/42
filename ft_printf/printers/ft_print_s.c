#include "ft_printf.h"

int	ft_print_s(char *s)
{
	int		i;
	char	*tmp;

	i = 0;
	if (!s)
	{
		tmp = "(null)";
		while (tmp[i])
		{
			ft_print_c(tmp[i]);
			i++;
		}
	}
	else
	{
		while (s[i])
		{
			ft_print_c(s[i]);
			i++;
		}
	}
	return (i);
}
