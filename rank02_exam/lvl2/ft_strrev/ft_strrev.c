char *ft_strrev(char *str)
{
	int i;
	int len;
	char tmp;

	len = 0;
	i = -1;
	tmp = str[i];
	while (str[len])
		len++;
	while (++i < --len)
	{
		str[i] = str[len];
		str[len] = tmp;
	}
	return (str);
}
