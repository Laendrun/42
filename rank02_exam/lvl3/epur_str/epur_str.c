#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;
	int	trigger;

	if (ac == 2)
	{
		i = 0;
		trigger = 0;
		while (av[1][i] == ' ' || av[1][i] == '\t')
			i++;
		while (av[1][i])
		{
			if (av[1][i] == ' ' || av[1][i] == '\t')
				trigger = 1;
			if (av[1][i] != ' ' && av[1][i] != '\t')
			{
				if (trigger)
					write(1, " ", 1);
				trigger = 0;
				write(1, &av[1][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
}