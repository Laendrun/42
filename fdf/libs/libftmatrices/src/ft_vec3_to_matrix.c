#include "libftmatrices.h"

float	**ft_vec3_to_matrix(t_vector3 v)
{
	float	**m;

	m = ft_create_matrix(&(t_vector2){3, 1});
	m[0][0] = v.x;
	m[1][0] = v.y;
	m[2][0] = v.z;
	return (m);
}
