#include "libftmatrices.h"

void	ft_print_matrix(float **a, t_vector2 *v)
{
	printf("%fx%f\n", v->x, v->y);
	printf("-------\n");
	for (int i = 0; i < v->x; i++) 
	{
		for (int j = 0; j < v->y; j++)
		{
			printf("%f ", a[i][j]);
		}
		printf("\n");
	}
}