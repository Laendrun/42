#include "libftmatrices.h"

t_vector3	ft_matrix_to_vec3(float **m)
{
	t_vector3	v;

	v.x = m[0][0];
	v.y = m[0][1];
	v.z = m[0][2];
	ft_free_matrix(m, 1);
	return (v);
}