#include "libftmatrices.h"

void	ft_print_vector3(t_vector3 v)
{
	printf("===== Vec 3D =====\n");
	printf("v->x : %f\n", v.x);
	printf("v->y : %f\n", v.y);
	printf("v->z : %f\n", v.z);
}