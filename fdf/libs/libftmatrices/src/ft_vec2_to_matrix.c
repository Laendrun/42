#include "libftmatrices.h"

float		**ft_vec2_to_matrix(t_vector2 v)
{
	float	**m = ft_create_matrix(&(t_vector2){3, 1});
	m[0][0] = v.x;
	m[1][0] = v.y;
	return (m);
}